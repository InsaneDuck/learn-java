package introduction.intro.basics;

public class doWhileDemo
{
	public static void main(String arg[])
	{
		int i=0;
		int j=0;
		System.out.println("do.......while");
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=5);
		System.out.println("while.....");
		while(j<=5)
		{
			System.out.println(j);
			j++;
		}
	}
}
